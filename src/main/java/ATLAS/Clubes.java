package ATLAS;

/**
 *
 * @author Connectis
 */
public class Clubes {
    
    private int codigo;
    private String nombre_club;
    private String nombre_fundador;
    private String año_fundacion;
    private String pais_origen;
    private String lema;
    private String colores;
    private int valor_suscripcion;

    /**
     * @return the codigo
     */
    public int getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    /**
     * @return the nombre_club
     */
    public String getNombre_club() {
        return nombre_club;
    }

    /**
     * @param nombre_club the nombre_club to set
     */
    public void setNombre_club(String nombre_club) {
        this.nombre_club = nombre_club;
    }

    /**
     * @return the nombre_fundador
     */
    public String getNombre_fundador() {
        return nombre_fundador;
    }

    /**
     * @param nombre_fundador the nombre_fundador to set
     */
    public void setNombre_fundador(String nombre_fundador) {
        this.nombre_fundador = nombre_fundador;
    }

    /**
     * @return the año_fundacion
     */
    public String getAño_fundacion() {
        return año_fundacion;
    }

    /**
     * @param año_fundacion the año_fundacion to set
     */
    public void setAño_fundacion(String año_fundacion) {
        this.año_fundacion = año_fundacion;
    }

    /**
     * @return the pais_origen
     */
    public String getPais_origen() {
        return pais_origen;
    }

    /**
     * @param pais_origen the pais_origen to set
     */
    public void setPais_origen(String pais_origen) {
        this.pais_origen = pais_origen;
    }

    /**
     * @return the lema
     */
    public String getLema() {
        return lema;
    }

    /**
     * @param lema the lema to set
     */
    public void setLema(String lema) {
        this.lema = lema;
    }

    /**
     * @return the colores
     */
    public String getColores() {
        return colores;
    }

    /**
     * @param colores the colores to set
     */
    public void setColores(String colores) {
        this.colores = colores;
    }

    /**
     * @return the valor_suscripcion
     */
    public int getValor_suscripcion() {
        return valor_suscripcion;
    }

    /**
     * @param valor_suscripcion the valor_suscripcion to set
     */
    public void setValor_suscripcion(int valor_suscripcion) {
        this.valor_suscripcion = valor_suscripcion;
    }
    
    
    
}
